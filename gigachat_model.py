def build_system_prompt(
    route: Route,
) -> str:
    """
    Собирает prompt только для выбранного маршрута.
    """

    route_prompt = ROUTE_PROMPTS.get(
        route,
        ROUTE_PROMPTS[Route.GENERAL],
    )

    examples = FEW_SHOT_EXAMPLES.get(
        route,
        FEW_SHOT_EXAMPLES[Route.GENERAL],
    )

    return (
        BASE_SYSTEM_PROMPT
        + "\n\n"
        + route_prompt.strip()
        + "\n\nПРИМЕРЫ:\n"
        + examples.strip()
    )


def call_gigachat(
    system_prompt: str,
    user_message: str,
    history=None,
) -> str:
    """
    Синхронный вызов GigaChat.

    Запускается через asyncio.to_thread,
    чтобы не блокировать Telegram event loop.
    """

    with GigaChat(
        credentials=GIGACHAT_KEY,
        scope="GIGACHAT_API_PERS",
        verify_ssl_certs=False,
        profanity_check=False,
    ) as giga:

        chat = Chat(
            messages=[
                Messages(
                    role="system",
                    content=system_prompt,
                )
            ]
        )

        if history:
            for message in history[-10:]:
                chat.messages.append(
                    Messages(
                        role=message.role,
                        content=message.content,
                    )
                )

        chat.messages.append(
            Messages(
                role="user",
                content=user_message,
            )
        )

        response = giga.chat(chat)

        if not response.choices:
            raise RuntimeError(
                "GigaChat вернул ответ без choices"
            )

        answer = response.choices[0].message.content

        if not answer:
            raise RuntimeError(
                "GigaChat вернул пустой ответ"
            )

        return answer.strip()


async def get_gigachat_response(
    user_message: str,
    history=None,
    route: Route = Route.GENERAL,
) -> str:
    """
    Получает один ответ от GigaChat.

    Повторных генераций и Thinking Engine нет.
    """

    try:
        return await asyncio.to_thread(
            call_gigachat,
            build_system_prompt(route),
            user_message,
            history,
        )

    except Exception:
        logger.exception(
            "Ошибка GigaChat: route=%s",
            route.value,
        )

        return (
            "Извини, сейчас я временно не могу ответить. "
            "Попробуй немного позже."
        )
