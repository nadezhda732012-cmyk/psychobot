def build_system_prompt(
    route: Route,
) -> str:
    """
    Объединяет базовые правила с инструкцией выбранного маршрута.
    """

    route_instruction = ROUTE_INSTRUCTIONS.get(
        route,
        ROUTE_INSTRUCTIONS[Route.GENERAL],
    )

    return (
        BASE_SYSTEM_PROMPT.strip()
        + "\n\n"
        + route_instruction.strip()
    )


async def get_gigachat_response(
    user_message: str,
    history=None,
    route: Route = Route.GENERAL,
) -> str:
    """
    Получает ответ GigaChat с учётом маршрута Router.
    """

    try:
        with GigaChat(
            credentials=GIGACHAT_KEY,
            scope="GIGACHAT_API_PERS",
            verify_ssl_certs=False,
            profanity_check=False,
        ) as giga:

            chat = Chat(messages=[])

            system_prompt = build_system_prompt(
                route=route,
            )

            chat.messages.append(
                Messages(
                    role="system",
                    content=system_prompt,
                )
            )

            if history:
                for msg in history[-10:]:
                    chat.messages.append(
                        Messages(
                            role=msg.role,
                            content=msg.content,
                        )
                    )

            chat.messages.append(
                Messages(
                    role="user",
                    content=user_message,
                )
            )

            response = giga.chat(chat)

            if response.choices:
                answer = response.choices[0].message.content

                if answer:
                    return answer.strip()

            logger.warning(
                "GigaChat вернул пустой ответ: route=%s",
                route.value,
            )

            return (
                "Не удалось получить ответ. "
                "Попробуй отправить сообщение ещё раз."
            )

    except Exception:
        logger.exception(
            "Ошибка GigaChat: route=%s",
            route.value,
        )

        return (
            "Извини, сейчас я временно не могу ответить. "
            "Попробуй немного позже."
        )
