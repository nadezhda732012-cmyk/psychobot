# --- Функции ---

def check_crisis(text: str) -> bool:
    """Проверка на кризисные ключевые слова"""
    crisis_words = ['суицид', 'убить себя', 'не хочу жить', 'покончить с собой', 'смерть', 
                    'хочу умереть', 'жизнь не имеет смысла', 'не вижу смысла']
    return any(word in text.lower() for word in crisis_words)


def detect_emotion(text: str) -> str:
    """Определяет эмоцию по тексту"""
    text_lower = text.lower()
    
    if any(word in text_lower for word in ['груст', 'печал', 'тоск', 'плак', 'обид']):
        return "грусть"
    if any(word in text_lower for word in ['тревог', 'боюс', 'страх', 'волн', 'беспоко']):
        return "тревога"
    if any(word in text_lower for word in ['зл', 'гнев', 'ярост', 'разозл']):
        return "гнев"
    if any(word in text_lower for word in ['одинок', 'пустот', 'никто не нужн']):
        return "одиночество"
    if any(word in text_lower for word in ['счаст', 'рад', 'здоров', 'весел', 'прекрасн']):
        return "радость"
    if any(word in text_lower for word in ['устал', 'вымотал', 'нет сил', 'истощ']):
        return "усталость"
    
    return None


def get_simple_response(user_message: str) -> str:
    """Генерирует эмпатичный ответ без использования ИИ"""
    
    # Проверяем приветствие
    if any(word in user_message.lower() for word in ['привет', 'здравствуй', 'хай', 'ку', 'доброе утро', 'добрый день']):
        return random.choice(GREETINGS)
    
    # Определяем эмоцию
    emotion = detect_emotion(user_message)
    
    if emotion and emotion in EMPATHY_RESPONSES:
        # Выбираем случайный ответ из категории
        response = random.choice(EMPATHY_RESPONSES[emotion])
        # Добавляем уточняющий вопрос
        follow_up = random.choice(FOLLOW_UP_QUESTIONS)
        return f"{response}\n\n{follow_up}"
    
    # Если эмоция не распознана — стандартный ответ + вопрос
    response = random.choice(DEFAULT_RESPONSES)
    follow_up = random.choice(FOLLOW_UP_QUESTIONS)
    return f"{response}\n\n{follow_up}"


async def get_response(user_id: int, user_message: str) -> str:
    """Главная функция, возвращающая ответ"""
    
    # 1. Проверка на кризис
    if check_crisis(user_message):
        save_message(user_id, 'user', user_message)
        crisis_response = f"""
Я слышу, что тебе очень тяжело. Пожалуйста, помни, что ты не один(а).
Твои чувства важны, и есть люди, которые готовы помочь прямо сейчас.

{CRISIS_CONTACTS}

Поговори с ними, это может быть первым шагом к облегчению.
Хочешь, мы можем продолжить разговор, но помни: я не заменяю профессиональную помощь.
"""
        save_message(user_id, 'assistant', crisis_response)
        return crisis_response
    
    # 2. Сохраняем сообщение пользователя
    save_message(user_id, 'user', user_message)
    
    # 3. Генерируем ответ
    bot_response = get_simple_response(user_message)
    
    # 4. Сохраняем ответ бота
    save_message(user_id, 'assistant', bot_response)
    
    return bot_response